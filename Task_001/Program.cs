// Задайте двумерный массив целых чисел. Напишите программу, которая
// находит максимальный элемент в каждой строке массива и выводит его.


using System;

class Program
{
    static void Main()
    {
        int[,] array = {
            {1, 3, 5},
            {7, 2, 8},
            {4, 6, 0}
        };

        for (int i = 0; i < array.GetLength(0); i++) // Проходим по строкам
        {
            int max = array[i, 0]; // Инициализируем максимальное значение
            for (int j = 1; j < array.GetLength(1); j++) // Проходим по столбцам
            {
                if (array[i, j] > max)
                {
                    max = array[i, j]; // Обновляем максимальное значение
                }
            }
            Console.WriteLine($"Максимальный элемент в строке {i}: {max}");
        }
    }
}

